CREATE TABLE denuncias (
    id                  INT GENERATED BY DEFAULT AS IDENTITY,
    codigo_seguimiento  VARCHAR(20)  NOT NULL,
    denunciante_id      INT          NOT NULL,
    tipo_delito_id      INT          NOT NULL,
    estado_id           INT          NOT NULL,
    distrito_fiscal_id  INT,
    ubigeo_id           INT,
    direccion_hecho     VARCHAR(300),
    fecha_hecho         TIMESTAMP    NOT NULL,
    descripcion_hecho   TEXT         NOT NULL,
    es_anonima          BOOLEAN      DEFAULT FALSE,
    created_at          TIMESTAMP    DEFAULT NOW(),
    updated_at          TIMESTAMP,

    CONSTRAINT pk_denuncias PRIMARY KEY (id),
    CONSTRAINT uq_denuncias_codigo UNIQUE (codigo_seguimiento),
    CONSTRAINT chk_denuncias_codigo_fmt CHECK (codigo_seguimiento ~ '^DEN-\d{4}-\d{5}$'),
    CONSTRAINT chk_denuncias_fecha_hecho CHECK (fecha_hecho <= NOW()),
    CONSTRAINT fk_denuncias_denunciante FOREIGN KEY (denunciante_id) REFERENCES personas (id) DEFERRABLE INITIALLY IMMEDIATE,
    CONSTRAINT fk_denuncias_tipo_delito FOREIGN KEY (tipo_delito_id) REFERENCES tipos_delito (id) DEFERRABLE INITIALLY IMMEDIATE,
    CONSTRAINT fk_denuncias_estado FOREIGN KEY (estado_id) REFERENCES estados_denuncia (id) DEFERRABLE INITIALLY IMMEDIATE,
    CONSTRAINT fk_denuncias_dist_fiscal FOREIGN KEY (distrito_fiscal_id) REFERENCES distritos_fiscales (id) DEFERRABLE INITIALLY IMMEDIATE,
    CONSTRAINT fk_denuncias_ubigeo FOREIGN KEY (ubigeo_id) REFERENCES ubigeo (id) DEFERRABLE INITIALLY IMMEDIATE
);